    public void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblTkSP.getModel();
        dtm.setRowCount(0);
        for (ThongKe tk : tkSer.getAll()) {
            Object rowData[] = {
                tk.getMaSP(),
                tk.getTenSP(),
                tk.getNamSX(),
                tk.getSoLuonTon(),
                tk.getGiaNhap(),
                tk.getGiaBan(),
                tk.getSoLuongBan(),
                tk.doanhThu()
            };
            dtm.addRow(rowData);
        }
    }

public void thongKeSp1(JPanel jpanItem){
    List<ThongKe> list = tkSer.getAll();
    if (list!= null) {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        for (ThongKe thongKe : list) {
            dataset.addValue(thongKe.getSoLuongBan(), "Số lượng bán", thongKe.getGiaBan());
        }
        JFreeChart tree  = ChartFactory.createBarChart("Thống kê sản phẩm", "Giá bán", "Số lượng bán", dataset);
        ChartPanel chartPanel = new ChartPanel(tree);
        chartPanel.setPreferredSize(new Dimension(jpanItem.getWidth(),0));
        
        jpanItem.removeAll();
        jpanItem.setLayout(new CardLayout());
        jpanItem.add(chartPanel);
        jpanItem.validate();
        jpanItem.repaint();
    }
}
 public void thongKeSp2(JPanel jpnItem) {
        List<ThongKe> listItem = tkSer.getAll();

        TaskSeriesCollection ds = new TaskSeriesCollection();
        JFreeChart ganttChart = ChartFactory.createGanttChart(
                "BIỂU ĐỒ THEO DÕI HOẠT ĐỘNG ",
                "Tên sản phẩm", "Só lương bán", ds, true, false, false
        );

        TaskSeries taskSeries;
        Task task;

        if (listItem != null) {
            for (ThongKe item : listItem) {
                taskSeries = new TaskSeries(item.getTenSP());
                task = new Task(item.getTenSP(), new SimpleTimePeriod(item.getSoLuonTon(), item.getSoLuongBan()));
                taskSeries.add(task);
                ds.add(taskSeries);
            }
        }
        ChartPanel chartPanel = new ChartPanel(ganttChart);
        chartPanel.setPreferredSize(new Dimension(jpnItem.getWidth(), 30));

        jpnItem.removeAll();
        jpnItem.setLayout(new CardLayout());
        jpnItem.add(chartPanel);
        jpnItem.validate();
        jpnItem.repaint();
}
